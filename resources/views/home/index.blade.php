		// ✅ visualViewport.height 유지
		const windowHeight = window.visualViewport
			? window.visualViewport.height
			: window.innerHeight;

		// ✅ 버퍼값만 조절 — 숫자 올리면 더 일찍, 내리면 더 늦게
		const triggerBuffer = $(window).width() < 768 ? 10 : 0;

